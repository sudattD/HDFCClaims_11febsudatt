public class InsuredDetailsModel {
    private String Master_Claim_Number;
    private String Insured_Name;
    private String Insured_Address;
    private String Insured_Pin_Code;
    private String Insured_Email;
    private String Insured_Mobile_Number;

    public String getMaster_Claim_Number() {
        return Master_Claim_Number;
    }

    public void setMaster_Claim_Number(String master_Claim_Number) {
        Master_Claim_Number = master_Claim_Number;
    }

    public String getInsured_Name() {
        return Insured_Name;
    }

    public void setInsured_Name(String insured_Name) {
        Insured_Name = insured_Name;
    }

    public String getInsured_Address() {
        return Insured_Address;
    }

    public void setInsured_Address(String insured_Address) {
        Insured_Address = insured_Address;
    }

    public String getInsured_Pin_Code() {
        return Insured_Pin_Code;
    }

    public void setInsured_Pin_Code(String insured_Pin_Code) {
        Insured_Pin_Code = insured_Pin_Code;
    }

    public String getInsured_Email() {
        return Insured_Email;
    }

    public void setInsured_Email(String insured_Email) {
        Insured_Email = insured_Email;
    }

    public String getInsured_Mobile_Number() {
        return Insured_Mobile_Number;
    }

    public void setInsured_Mobile_Number(String insured_Mobile_Number) {
        Insured_Mobile_Number = insured_Mobile_Number;
    }
}
